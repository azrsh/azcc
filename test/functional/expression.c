int assert(int, int, char *);
int printf();

int main() {
  assert(0, 0, "0");
  assert(42, 42, "42");
  assert(21, 5 + 20 - 4, "5 + 20 - 4");
  assert(41, 12 + 34 - 5, "12 + 34 - 5");
  assert(47, 5 + 6 * 7, "5 + 6 * 7");
  assert(15, 5 * (9 - 6), "5 * (9 - 6)");
  assert(4, (3 + 5) / 2, "(3 + 5) / 2");
  assert(10, -10 + 20, "-10 + 20");
  assert(2, -10 / -5, "-10 / -5");
  assert(1, 10 == 10, "10 - 10");
  assert(0, 10 < 10, "10 < 10");
  assert(1, 20 - 10 * 1 + 2 <= 2 * 7, "20 - 10 * 1 + 2 <= 2 * 7");
  assert(0, 80 - 50 > 20 * 2, "80 - 50 > 20 * 2");
  assert(1, 80 - 50 < 20 * 2, "80 - 50 < 20 * 2");
  assert(0, 80 - 0 <= 20 * 2, "80 - 0 <= 20 * 2");
  assert(1, 0 <= 16, "0 <= 16");
  assert(0, 0 >= 16, "0 >= 16");
  assert(0, 80 % 4, "80 % 4");
  assert(3, 79 % 4, "79 % 4");
  printf("OK\n");
  return 0;
}
